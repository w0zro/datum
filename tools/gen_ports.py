#!/usr/bin/env python3
"""Generate per-app color ports from the derived palette.

The palette in derive.py is the source of truth. Every port here is generated
from it, so a port cannot silently drift out of sync with the scheme (which is
exactly how a stale purple survived in the first hand-written Ghostty theme).

    python3 tools/gen_ports.py          # write ports/
    python3 tools/gen_ports.py --check  # fail if ports/ is stale (CI-friendly)

Terminals and editors get separate dark/light files (they can't follow the OS
on their own). Shell/CLI tools that run *inside* a terminal are emitted once,
using ANSI color indices (0-15) so they inherit the terminal's datum theme and
follow light/dark automatically.
"""
import json
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from derive import palette  # noqa: E402

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
MODES = ("dark", "light")


def pal(mode):
    return {k: v["hex"] for k, v in palette(mode).items()}


# ANSI slot -> palette role. normal 1-6 = tier-1 accents; bright 9-14 carry the
# colors ANSI otherwise can't reach: orange (no slot of its own) + the tier-2
# glue, so the loud/quiet tier system reaches any 16-color tool.
ANSI = {1: "red", 9: "orange", 2: "green", 10: "green", 3: "yellow", 11: "var",
        4: "blue", 12: "op", 5: "purple", 13: "call", 6: "cyan", 14: "param",
        8: "fg1"}
NEUTRAL = {
    "dark":  {0: "#2b2f35", 7: "#dbe0e8", 15: "#eef2f7"},
    "light": {0: "#292e35", 7: "#ced3d9", 15: "#e7ecf2"},
}


def ansi16(mode):
    p = pal(mode)
    return [NEUTRAL[mode].get(i) or p[ANSI[i]] for i in range(16)]


GEN = "GENERATED by tools/gen_ports.py from tools/derive.py -- do not edit by hand."

# ---------------------------------------------------------------- ghostty
def ghostty(mode):
    p, a = pal(mode), ansi16(mode)
    note = ""
    if mode == "light":
        note = ("\n# On the light ground the tier-2 glue is darker than tier-1 (it is the body\n"
                "# text, so it holds the higher contrast) -- here 'bright' means more prominent.")
    lines = [f"# datum {mode} -- {GEN}{note}",
             f"background = {p['bg0']}", f"foreground = {p['fg0']}",
             f"cursor-color = {p['purple']}", f"cursor-text = {p['bg0']}",
             f"selection-background = {p['bg2']}", f"selection-foreground = {p['fg0']}", ""]
    lines += [f"palette = {i}={a[i]}" for i in range(16)]
    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------- alacritty (TOML)
def alacritty(mode):
    p, a = pal(mode), ansi16(mode)
    n, b = a[0:8], a[8:16]
    q = lambda h: f'"{h}"'  # noqa: E731
    return (
        f"# datum {mode} -- {GEN}\n"
        "[colors.primary]\n"
        f"background = {q(p['bg0'])}\nforeground = {q(p['fg0'])}\n\n"
        "[colors.cursor]\n"
        f"cursor = {q(p['purple'])}\ntext = {q(p['bg0'])}\n\n"
        "[colors.selection]\n"
        f"background = {q(p['bg2'])}\ntext = {q(p['fg0'])}\n\n"
        "[colors.normal]\n"
        f"black = {q(n[0])}\nred = {q(n[1])}\ngreen = {q(n[2])}\nyellow = {q(n[3])}\n"
        f"blue = {q(n[4])}\nmagenta = {q(n[5])}\ncyan = {q(n[6])}\nwhite = {q(n[7])}\n\n"
        "[colors.bright]\n"
        f"black = {q(b[0])}\nred = {q(b[1])}\ngreen = {q(b[2])}\nyellow = {q(b[3])}\n"
        f"blue = {q(b[4])}\nmagenta = {q(b[5])}\ncyan = {q(b[6])}\nwhite = {q(b[7])}\n"
    )


# ---------------------------------------------------------------- kitty (conf)
def kitty(mode):
    p, a = pal(mode), ansi16(mode)
    lines = [f"# datum {mode} -- {GEN}",
             f"background {p['bg0']}", f"foreground {p['fg0']}",
             f"cursor {p['purple']}", f"cursor_text_color {p['bg0']}",
             f"selection_background {p['bg2']}", f"selection_foreground {p['fg0']}",
             f"url_color {p['blue']}", ""]
    lines += [f"color{i} {a[i]}" for i in range(16)]
    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------- wezterm (TOML scheme)
def wezterm(mode):
    p, a = pal(mode), ansi16(mode)
    arr = lambda xs: "[" + ", ".join(f'"{x}"' for x in xs) + "]"  # noqa: E731
    return (
        f"# datum {mode} -- {GEN}\n"
        f"# Drop in ~/.config/wezterm/colors/ ; select with color_scheme = 'datum-{mode}'\n"
        "[colors]\n"
        f'foreground = "{p["fg0"]}"\nbackground = "{p["bg0"]}"\n'
        f'cursor_bg = "{p["purple"]}"\ncursor_fg = "{p["bg0"]}"\ncursor_border = "{p["purple"]}"\n'
        f'selection_bg = "{p["bg2"]}"\nselection_fg = "{p["fg0"]}"\n'
        f"ansi = {arr(a[0:8])}\nbrights = {arr(a[8:16])}\n\n"
        "[metadata]\n"
        f'name = "datum-{mode}"\n'
    )


# ---------------------------------------------------------------- tmux (ANSI, theme-following)
def tmux_theme():
    return (
        f"# datum -- {GEN}\n"
        "# source-file this from ~/.tmux.conf. Uses ANSI indices so it follows\n"
        "# the terminal's datum theme (and light/dark) automatically.\n"
        'set -g status-style "bg=default,fg=color8"\n'
        'set -g status-left ""\n'
        'set -g status-right "#[fg=color8] #S "\n'
        'set -g window-status-format "#[fg=color8] #I:#W "\n'
        'set -g window-status-current-format "#[fg=color4,bold] #I:#W "\n'
        'set -g pane-border-style "fg=color0"\n'
        'set -g pane-active-border-style "fg=color4"\n'
        'set -g mode-style "bg=color0,fg=color7"\n'
        'set -g message-style "bg=color0,fg=color3"\n'
        'set -g message-command-style "bg=color0,fg=color3"\n'
    )


# ---------------------------------------------------------------- fzf (ANSI, theme-following)
def fzf_theme():
    opts = ("--color=fg:-1,bg:-1,hl:4"
            ",fg+:-1,bg+:0,hl+:4"
            ",info:8,border:0,separator:0"
            ",prompt:5,pointer:4,marker:2,spinner:6,header:8,gutter:-1")
    return (
        f"# datum -- {GEN}\n"
        "# source this (or append the flags to FZF_DEFAULT_OPTS). ANSI indices\n"
        "# mean it follows the terminal's datum theme automatically.\n"
        f'export FZF_DEFAULT_OPTS="${{FZF_DEFAULT_OPTS:-}} {opts}"\n'
    )


