# ---------------------------------------------------------------- bat (.tmTheme plist)
def bat(mode):
    p = pal(mode)
    rules = [
        ("comment", p["fg1"], "italic"),
        ("string", p["green"], None),
        ("constant.numeric", p["orange"], None),
        ("constant.language, constant.character, support.constant", p["yellow"], None),
        ("keyword, storage", p["blue"], None),
        ("keyword.operator", p["op"], None),
        ("entity.name.function, support.function", p["purple"], None),
        ("entity.name.type, entity.name.class, support.type", p["cyan"], None),
        ("variable", p["var"], None),
        ("variable.parameter", p["param"], None),
        ("punctuation", p["fg1"], None),
        ("entity.name.tag", p["purple"], None),
        ("invalid", p["red"], None),
    ]
    body = [
        "    <dict>",
        "      <key>settings</key>",
        "      <dict>",
        f"        <key>background</key><string>{p['bg0']}</string>",
        f"        <key>foreground</key><string>{p['fg0']}</string>",
        f"        <key>caret</key><string>{p['purple']}</string>",
        f"        <key>selection</key><string>{p['bg2']}</string>",
        f"        <key>lineHighlight</key><string>{p['bg1']}</string>",
        "      </dict>",
        "    </dict>",
    ]
    for scope, color, style in rules:
        body.append("    <dict>")
        body.append(f"      <key>scope</key><string>{scope}</string>")
        body.append("      <key>settings</key>")
        body.append("      <dict>")
        body.append(f"        <key>foreground</key><string>{color}</string>")
        if style:
            body.append(f"        <key>fontStyle</key><string>{style}</string>")
        body.append("      </dict>")
        body.append("    </dict>")
    return (
        '<?xml version="1.0" encoding="UTF-8"?>\n'
        '<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" '
        '"http://www.apple.com/DTDs/PropertyList-1.0.dtd">\n'
        '<plist version="1.0">\n<dict>\n'
        f"  <key>name</key><string>datum-{mode}</string>\n"
        "  <key>settings</key>\n  <array>\n" + "\n".join(body) + "\n  </array>\n"
        "</dict>\n</plist>\n"
    )


# ---------------------------------------------------------------- iterm2 (.itermcolors plist)
def _rgb01(h):
    h = h.lstrip("#")
    return [int(h[i:i + 2], 16) / 255 for i in (0, 2, 4)]


def iterm(mode):
    """iTerm2 .itermcolors -- the source format for the iTerm2-Color-Schemes repo,
    the upstream that feeds many terminals' built-in theme pickers. Submitted there
    as a one-off manual PR; generated here so it never drifts from the palette."""
    p, a = pal(mode), ansi16(mode)
    colors = {f"Ansi {i} Color": a[i] for i in range(16)}
    colors.update({
        "Background Color": p["bg0"], "Foreground Color": p["fg0"], "Bold Color": p["fg0"],
        "Cursor Color": p["purple"], "Cursor Text Color": p["bg0"], "Cursor Guide Color": p["bg1"],
        "Selection Color": p["bg2"], "Selected Text Color": p["fg0"],
        "Link Color": p["blue"], "Badge Color": p["red"],
    })
    order = [f"Ansi {i} Color" for i in range(16)] + [
        "Background Color", "Bold Color", "Cursor Color", "Cursor Guide Color",
        "Cursor Text Color", "Foreground Color", "Link Color", "Selected Text Color",
        "Selection Color", "Badge Color"]

    def entry(key):
        r, g, b = _rgb01(colors[key])
        return (f"  <key>{key}</key>\n  <dict>\n"
                "    <key>Color Space</key><string>sRGB</string>\n"
                f"    <key>Red Component</key><real>{r}</real>\n"
                f"    <key>Green Component</key><real>{g}</real>\n"
                f"    <key>Blue Component</key><real>{b}</real>\n"
                "    <key>Alpha Component</key><real>1</real>\n  </dict>")

    return (
        '<?xml version="1.0" encoding="UTF-8"?>\n'
        '<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" '
        '"http://www.apple.com/DTDs/PropertyList-1.0.dtd">\n'
        '<plist version="1.0">\n'
        # XML comments can't contain "--", so no shared GEN marker here.
        f"<!-- datum-{mode} : generated by tools/gen_ports.py from tools/derive.py; do not edit by hand -->\n"
        "<dict>\n" + "\n".join(entry(k) for k in order) + "\n</dict>\n</plist>\n"
    )


# ---------------------------------------------------------------- registry
def _per_mode(app, fn, ext):
    return {f"ports/{app}/datum-{m}.{ext}": (lambda m=m: fn(m)) for m in MODES}


PORTS = {}
PORTS.update({f"ports/ghostty/datum-{m}": (lambda m=m: ghostty(m)) for m in MODES})
PORTS.update(_per_mode("alacritty", alacritty, "toml"))
PORTS.update(_per_mode("kitty", kitty, "conf"))
PORTS.update(_per_mode("wezterm", wezterm, "toml"))
# VS Code themes live inside the extension folder so `vsce package` / a symlink
# into ~/.vscode/extensions makes them installable (see ports/vscode/package.json).
PORTS.update({f"ports/vscode/themes/datum-{m}.json": (lambda m=m: vscode(m)) for m in MODES})
PORTS.update(_per_mode("bat", bat, "tmTheme"))
# iTerm2 scheme files — not applied locally; generated for the upstream PR to
# iTerm2-Color-Schemes (feeds Ghostty/WezTerm/Kitty/Alacritty built-in pickers).
PORTS.update(_per_mode("iterm", iterm, "itermcolors"))
PORTS["ports/tmux/datum.tmux"] = tmux_theme
PORTS["ports/fzf/datum.sh"] = fzf_theme
PORTS["ports/delta/datum.gitconfig"] = delta_theme
PORTS["ports/shell/datum.sh"] = shell_theme


def main():
    check = "--check" in sys.argv
    stale = []
    for rel, render in sorted(PORTS.items()):
        path = os.path.join(ROOT, rel)
        content = render()
        current = open(path).read() if os.path.exists(path) else None
        if check:
            if current != content:
                stale.append(rel)
            continue
        os.makedirs(os.path.dirname(path), exist_ok=True)
        with open(path, "w") as fh:
            fh.write(content)
        print(("unchanged " if current == content else "wrote     ") + rel)
    if check:
        print("STALE: " + ", ".join(stale) if stale else "all ports up to date")
        return 1 if stale else 0
    return 0


if __name__ == "__main__":
    sys.exit(main())
